def main(csv_out=None, html_out=None):
    print("=== CO2 Emission PoC - API Demo Flow ===\n")

    # --- Step 0: health ---
    print("[0] Health check")
    r = client.get("/health")
    check(r.status_code == 200, f"GET /health → 200 (got {r.status_code})")
    check(r.json()["status"] == "ok", "status == ok")

    # --- Step 1: upload ---
    print("\n[1] Upload accounting CSV")
    with open(FIXTURE, "rb") as f:
        r = client.post(
            "/api/v1/uploads",
            files={"file": ("accounting_sample.csv", f, "text/csv")},
            data={"data_type": "accounting"},
        )
    check(r.status_code == 201, f"POST /api/v1/uploads → 201 (got {r.status_code})")
    data = r.json()
    upload_id = data["upload_id"]
    check(data["row_count"] == 5, f"row_count == 5 (got {data['row_count']})")
    check(data["status"] == "pending_mapping", "status == pending_mapping")
    print(f"       upload_id={upload_id}")

    # --- Step 2: mapping ---
    print("\n[2] Save column mapping")
    r = client.post(
        f"/api/v1/uploads/{upload_id}/mappings",
        json={"mappings": MAPPING},
    )
    check(r.status_code == 201, f"POST /mappings → 201 (got {r.status_code})")
    mapping_id = r.json()["mapping_id"]
    print(f"       mapping_id={mapping_id}")

    # --- Step 3: calculation ---
    print("\n[3] Run calculation (factor version 2023)")
    r = client.post(
        "/api/v1/calculations",
        json={"upload_id": upload_id, "emission_factor_version": "2023"},
    )
    check(r.status_code == 202, f"POST /calculations → 202 (got {r.status_code})")
    job = r.json()
    job_id = job["job_id"]
    check(job["status"] == "completed", f"job.status == completed (got {job['status']})")
    print(f"       job_id={job_id}")

    # --- Step 4: results ---
    print("\n[4] Fetch results")
    r = client.get(f"/api/v1/calculations/{job_id}/results")
    check(r.status_code == 200, f"GET /results → 200 (got {r.status_code})")
    result = r.json()
    rows = result["rows"]
    summary = result["summary"]

    check(len(rows) == 5, f"5 result rows (got {len(rows)})")
    check(summary["grand_total"] > 0, f"grand_total > 0 (got {summary['grand_total']})")
    check(
        abs(summary["scope2_total"] - 453.0) < 0.1,
        f"scope2 == 453.0 kg-CO2e (electricity 1000kWh × 0.453) got {summary['scope2_total']}",
    )
    check(
        abs(summary["scope1_total"] - 563.5) < 0.1,
        f"scope1 == 563.5 kg-CO2e (gas 150m³ + gasoline 100L) got {summary['scope1_total']}",
    )

    print("\n  Scope summary:")
    print(f"    Scope 1: {summary['scope1_total']:>8.2f} kg-CO2e")
    print(f"    Scope 2: {summary['scope2_total']:>8.2f} kg-CO2e")
    print(f"    Scope 3: {summary['scope3_total']:>8.2f} kg-CO2e")
    print(f"    TOTAL  : {summary['grand_total']:>8.2f} kg-CO2e")
    print(f"    Rows   : {summary['calculated_row_count']} calculated, {summary['unclassified_count']} unclassified")

    # --- Step 5: recalculation ---
    print("\n[5] Recalculate with factor version 2021")
    r = client.post(
        f"/api/v1/calculations/{job_id}/recalculate",
        json={"emission_factor_version": "2021"},
    )
    check(r.status_code == 202, f"POST /recalculate → 202 (got {r.status_code})")
    job2_id = r.json()["job_id"]
    r2 = client.get(f"/api/v1/calculations/{job2_id}/results")
    total2 = r2.json()["summary"]["grand_total"]
    check(total2 != summary["grand_total"], f"recalc total differs (2023={summary['grand_total']}, 2021={total2})")
    print(f"       2021 grand_total={total2:.2f} kg-CO2e (vs 2023: {summary['grand_total']:.2f})")

    # --- Step 5b: comparison / delta ---
    print("\n[5b] Compare 2023 vs 2021 (Delta)")
    r = client.get(f"/api/v1/comparisons?job_ids={job_id},{job2_id}")
    check(r.status_code == 200, f"GET /comparisons → 200 (got {r.status_code})")
    cmp = r.json()
    check("comparisons" in cmp, "response has 'comparisons'")
    check(len(cmp["comparisons"]) == 2, f"2 entries (got {len(cmp['comparisons'])})")
    check("delta" in cmp, "response has 'delta'")
    delta = cmp["delta"]
    print(f"       Delta  scope1={delta['scope1']:+.2f}  scope2={delta['scope2']:+.2f}  scope3={delta['scope3']:+.2f}  total={delta['grand_total']:+.2f}")

    # --- Step 6: CSV export ---
    print("\n[6] CSV export")
    r = client.get(f"/api/v1/reports/{job_id}/csv")
    check(r.status_code == 200, f"GET /reports/csv → 200 (got {r.status_code})")
    check("排出量(kg-CO2e)" in r.text, "CSV contains header '排出量(kg-CO2e)'")
    check("Scope2合計" in r.text, "CSV contains '集計' section")
    lines = [l for l in r.text.splitlines() if l.strip()]
    print(f"       {len(lines)} lines in CSV")

    # --- Step 7: write CSV artifact ---
    if csv_out:
        print(f"\n[7] Writing CSV report to {csv_out}")
        r = client.get(f"/api/v1/reports/{job_id}/csv")
        check(r.status_code == 200, f"GET /reports/csv for file export → 200 (got {r.status_code})")
        with open(csv_out, "w", encoding="utf-8") as fh:
            fh.write(r.text)
        check(os.path.getsize(csv_out) > 0, f"{csv_out} written with content")
        print(f"       Wrote {os.path.getsize(csv_out)} bytes")

    # --- Step 8: write HTML report ---
    if html_out:
        print(f"\n[8] Writing HTML report to {html_out}")
        generate_html_report(summary, html_out)
        check(os.path.getsize(html_out) > 0, f"{html_out} written with content")
        print(f"       Wrote {os.path.getsize(html_out)} bytes")

    print("\n=== ALL CHECKS PASSED ===")
    print(f"Grand total: {summary['grand_total']:.2f} kg-CO2e from accounting_sample.csv")


if __name__ == "__main__":
    parser = argparse.ArgumentParser()
    parser.add_argument("--csv-out", metavar="PATH", help="Write CSV report to this path")
    parser.add_argument("--html-out", metavar="PATH", help="Write HTML report to this path")
    args = parser.parse_args()
    main(csv_out=args.csv_out, html_out=args.html_out)
