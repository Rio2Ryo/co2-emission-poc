#!/usr/bin/env python3
"""
Demo script: upload -> mapping -> calculation -> results
Uses FastAPI TestClient (no server needed).
Exit 0 on success, 1 on failure.
"""
import argparse
import json
import os
import sys

# Ensure app is importable
sys.path.insert(0, os.path.dirname(os.path.dirname(os.path.abspath(__file__))))

os.environ.setdefault("DATABASE_URL", "sqlite:////tmp/co2poc_demo.db")

from app.main import app
from app.db.base import Base
from app.db.session import engine, get_db, SessionLocal
from fastapi.testclient import TestClient
from sqlalchemy.orm import Session

# Fresh DB for demo
Base.metadata.drop_all(bind=engine)
Base.metadata.create_all(bind=engine)

client = TestClient(app)

FIXTURE = os.path.join(
    os.path.dirname(__file__), "..", "tests", "fixtures", "accounting_sample.csv"
)
MAPPING = {
    "勘定科目コード": "account_code",
    "勘定科目名":     "account_name",
    "数量":           "quantity",
    "単位":           "unit",
    "金額":           "amount",
}


def check(condition, msg):
    if not condition:
        print(f"  FAIL: {msg}", file=sys.stderr)
        sys.exit(1)
    print(f"  OK:   {msg}")


